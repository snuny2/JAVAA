package w0926;

public class SimpleNethod {

	static void print_head() {
		System.out.println("------------------------------------------------------------------------------------");		
	}
	public static void main(String[] args) {
		print_head();
		System.out.println("메소드를 이용한 프로그램");
		print_head();
		System.out.println("메소드를 이용하면 프로그램을 구조화 가능");
	}

}
